// Logical Operator
// && Logical AND : true  if both the operands are true,else returns false
// || Logical OR : true if either of the operands is true,returns false if both are false
// ! Logical NOT

console.log(1 === 1 && 2 === "2");
console.log(1 === "1" || 2 === "2");
console.log(!(2 === "2"));
